/**
 * Core package entry point
 *
 * @since 0.0.1
 */

/**
 * Adds two numbers together
 *
 * @since 0.0.1
 */
export const add = (a: number, b: number): number => a + b;

/**
 * Subtracts the second number from the first
 *
 * @since 0.0.1
 */
export const subtract = (a: number, b: number): number => a - b;
